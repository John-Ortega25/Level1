
public class WarmUp {
	public static void main(String[] args) {
		String name= "David";
		System.out.println(name);
		System.out.println(name + " Ramirez");
		System.out.println(name);
		name= name+ " Calderon";
		System.out.println(name);
		int age=10;
		System.out.println(age + 7);
		System.out.println(age);
		System.out.println(age++);
		System.out.println(age);
		System.out.println(++age);
		System.out.println(age);
	}
}
